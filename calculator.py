# making a calculator 
a= int(input('enter a number:'))
b= int(input(" enter second number:"))
print('the value of', a , '+' , b,'is:' , a+b)
print('the value of', a , '-' , b,'is:' , a-b)
print('the value of', a , '*' , b,'is:' , a*b)
print('the value of', a , '/' , b,'is:' , a/b)
print('the value of', a , '//' , b,'is:' , a//b)
